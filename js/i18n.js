/* ===================================================
   CORE TRANSLATE FUNCTION
   =================================================== */
function applyTranslations(lang) {
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (translations[lang][key] !== undefined) el.innerHTML = translations[lang][key];
  });
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key] !== undefined) el.textContent = translations[lang][key];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (translations[lang][key] !== undefined) el.setAttribute('placeholder', translations[lang][key]);
  });
}

/* ===================================================
   LANGUAGE TOGGLE INIT
   =================================================== */
function initLangToggle() {
  const STORAGE_KEY = 'kuncan_lang';
  const htmlEl      = document.documentElement;
  const toggleBtn   = document.getElementById('langToggle');
  if (!toggleBtn) return;

  let currentLang = localStorage.getItem(STORAGE_KEY) || 'tr';

  function setLang(lang) {
    currentLang = lang;
    htmlEl.setAttribute('lang', lang);
    localStorage.setItem(STORAGE_KEY, lang);
    applyTranslations(lang);

    const active = toggleBtn.querySelector('.lang-active');
    const other  = toggleBtn.querySelector('.lang-other');
    active.textContent = lang === 'tr' ? 'TR' : 'EN';
    other.textContent  = lang === 'tr' ? 'EN' : 'TR';

    const waTooltip = document.querySelector('.whatsapp-tooltip');
    if (waTooltip) waTooltip.textContent = translations[lang]['wa_tooltip'];
  }

  toggleBtn.addEventListener('click', () => setLang(currentLang === 'tr' ? 'en' : 'tr'));
  setLang(currentLang);
}

document.addEventListener('DOMContentLoaded', initLangToggle);
